// What is variable ?
// -> Variable are  used to store data in JavaScript.

// Variable declaration ?

// keyword : var -
var number = 10;
console.log(number); // ans: 10

// keyword : let -
let personName = "Paul";
console.log(personName); // ans: Paul

// keyword : const -
let cityName = "Dhaka";
console.log(cityName); // Dhaka

// object declaration
const names = {
  name: "Paul",
  age: 26,
};
console.log(names); // {name: 'Paul', age: 26}

// array declaration
const nums = [1, 2, 3, 4, 5, 6];
console.log(nums); // [1, 2, 3, 4, 5, 6]
